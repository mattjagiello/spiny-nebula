import express, { type Request, Response, NextFunction } from "express";
import routes from "./routes_new";
import backgroundRoutes from "./routes_background";
import fastRoutes from "./routes_fast";
import simpleRoutes from "./routes_simple";
import playlistRoutes from "./routes_playlists";
import { setupVite, serveStatic, log } from "./vite";

